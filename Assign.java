public class Assign {

  public static void main(String[] args) {
    // Deklarasi int
    int x, y;

    // Deklarasi float
    float z = 3.414f;

    // Deklarasi double
    double w = 3.1415;

    // Declare and assign boolean
    boolean truth = true;

    // Declare character variable
    char c;

    // Declare String variable
    String str;

    // Declare and assign String variable
    String str1 = "bye";

    // Assign value to char variable
    c = 'A';

    // Assign value to String variable
    str = "Hi out there!";

    // Assign values to int variables
    x = 6;
    y = 1000;

    // print value x dan y
    System.out.println("Value of x: " + x);
    System.out.println("Value of y: " + y);

    System.out.println("Value of z: " + z);
    System.out.println("Value of w: " + w);
    System.out.println("Value of truth: " + truth);
    System.out.println("Value of c: " + c);
    System.out.println("Value of str: " + str);
    System.out.println("Value of str1: " + str1);
  }
}
